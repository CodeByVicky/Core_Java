package Final_Exam_Basic;

public class Types_Casting {

	public static void main(String[] args) {
	
		byte b=123;
		short s=b;
		System.out.println(s);
		
		byte y=(byte)s;

		System.out.println(y);
		
		int i=90;
		float f=i;
		System.out.println(f);
		
		int n=(int)f;
		System.out.println(n);
	}

}
